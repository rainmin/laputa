    /**
     * 获取错误信息
     *
     * @param e
     * @return
     */
    public static String getErrorMsg(Throwable e) {
        String errorMsg;
        if (e instanceof HttpException) {
            HttpException httpException = (HttpException) e;
            int code = httpException.code();
            errorMsg = "http异常，状态码：" + code;
        } else if (e instanceof ConnectException) {
            errorMsg = "网络连接异常，请检查网络";
        } else if (e instanceof SocketTimeoutException) {
            errorMsg = "网络连接超时，请检查网络";
        } else {
            errorMsg = "发生未知错误：" + e.getMessage();
        }
        return errorMsg;
    }

    public static String getLoginErrorMsg(Throwable e) {
        if (e instanceof HttpException) {
            return "用户名或密码错误";
        }
        return getErrorMsg(e);
    }

    public static <E> void addAndRemoveDuplicate(List<E> fromList, List<E> toList) {
        if (fromList == null || toList == null) {
            return;
        }
        ArrayList<E> tmpList = new ArrayList<E>(toList);
        for (E fromO : fromList) {
            boolean has = false;
            for (E toO : tmpList) {
                if (fromO.equals(toO)) {
                    has = true;
                    break;
                }
            }
            if (!has) {
                toList.add(fromO);
            }
        }
    }

    public static String string2Json(String s) {
        StringBuffer sb = new StringBuffer();
        for (int i = 0; i < s.length(); i++) {
            char c = s.charAt(i);
            switch (c) {
                case '\"':
                    sb.append("\\\"");
                    break;
                case '\\':
                    sb.append("\\\\");
                    break;
                case '/':
                    sb.append("\\/");
                    break;
                case '\b':
                    sb.append("\\b");
                    break;
                case '\f':
                    sb.append("\\f");
                    break;
                case '\n':
                    sb.append("\\n");
                    break;
                case '\r':
                    sb.append("\\r");
                    break;
                case '\t':
                    sb.append("\\t");
                    break;
                default:
                    sb.append(c);
            }
        }
        return sb.toString();
    }

    public static String getJsonStringFromAssets(Context context, String fileName) {
        //将json数据变成字符串
        StringBuilder stringBuilder = new StringBuilder();
        try {
            //获取assets资源管理器
            AssetManager assetManager = context.getAssets();
            //通过管理器打开文件并读取
            BufferedReader bf = new BufferedReader(new InputStreamReader(
                    assetManager.open(fileName)));
            String line;
            while ((line = bf.readLine()) != null) {
                stringBuilder.append(line);
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return stringBuilder.toString();
    }

    public static Bitmap getBitmapFromDrawable(Context context, @DrawableRes int drawableId) {
        Drawable drawable = ContextCompat.getDrawable(context, drawableId);
        if (drawable instanceof BitmapDrawable) {
            return ((BitmapDrawable) drawable).getBitmap();
        } else if (drawable instanceof VectorDrawable || drawable instanceof VectorDrawableCompat) {
            Bitmap bitmap = Bitmap.createBitmap(drawable.getIntrinsicWidth(), drawable.getIntrinsicHeight(), Bitmap.Config.ARGB_8888);
            Canvas canvas = new Canvas(bitmap);
            drawable.setBounds(0, 0, canvas.getWidth(), canvas.getHeight());
            drawable.draw(canvas);
            return bitmap;
        } else {
            throw new IllegalArgumentException("unsupported drawable type");
        }
    }
